aVar = int(input("a = "))
bVar = int(input("b = "))

print("%d / %d = %f" % (aVar, bVar, aVar / bVar))
print("%d ** %d = %d" % (aVar, bVar, aVar ** bVar))
print("%d %% %d = %d" % (aVar, bVar, aVar % bVar))
print("%d // %d = %d" % (aVar, bVar, aVar // bVar))


